package DailyProgramP;

public class SubString {


    public static void main(String[] args) {
        String input = "JSP";
        System.out.println("Printing form index 0 to 2  : " + input.substring(0,2));
        System.out.println("Printing form index 2 to 2  : " + input.substring(2,2));
        System.out.println("Printing form index 2 to 3  : " + input.substring(2,3));


    }
}
